<?php

namespace VCR\Event;

use VCR\Request;
use Symfony\Contracts\EventDispatcher\Event;

class BeforeHttpRequestEvent extends Event
{
    /**
     * @var Request
     */
    protected $request;

    /**
     * @param Request $request
     */
    public function __construct(Request $request)
    {
        $this->request = $request;
    }

    /**
     * @return Request
     */
    public function getRequest()
    {
        return $this->request;
    }
}
